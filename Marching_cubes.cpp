void Triangle(GLPoint p0, GLPoint p1, GLPoint p2, GLPoint p3, GLPoint p4, GLPoint p5, GLPoint p6, GLPoint p7) {

    GLint cubeindex = 0;
    extern GLint triTable[256][16]; 

    if (p0.value) {
        cubeindex |= 1; 
    }

    if (p1.value) {
        cubeindex |= 2;
    }

    if (p2.value) {
        cubeindex |= 4;
    }

    if (p3.value) {
        cubeindex |= 8;
    }

    if (p4.value) {
        cubeindex |= 16;
    }

    if (p5.value) {
        cubeindex |= 32;
    }

    if (p6.value) {
        cubeindex |= 64;
    }

    if (p7.value) {
        cubeindex |= 128;
    }

    GLint connections[16];
    for (int i = 0; i < 16; i++) {
        connections[i] = triTable[cubeindex][i];
    }

    GLPoint maps[12];

    //Midpoint of edge0
    maps[0].X = (p0.X + p1.X) / 2.0f;
    maps[0].Y = (p0.Y + p1.Y) / 2.0f;
    maps[0].Z = (p0.Z + p1.Z) / 2.0f;

    //Midpoint of edge1
    maps[1].X = (p2.X + p1.X) / 2.0f;
    maps[1].Y = (p2.Y + p1.Y) / 2.0f;
    maps[1].Z = (p2.Z + p1.Z) / 2.0f;

    //Midpoint of edge2
    maps[2].X = (p2.X + p3.X) / 2.0f;
    maps[2].Y = (p2.Y + p3.Y) / 2.0f;
    maps[2].Z = (p2.Z + p3.Z) / 2.0f;

    //Midpoint of edge3
    maps[3].X = (p0.X + p3.X) / 2.0f;
    maps[3].Y = (p0.Y + p3.Y) / 2.0f;
    maps[3].Z = (p0.Z + p3.Z) / 2.0f;

    //Midpoint of edge4
    maps[4].X = (p4.X + p5.X) / 2.0f;
    maps[4].Y = (p4.Y + p5.Y) / 2.0f;
    maps[4].Z = (p4.Z + p5.Z) / 2.0f;

    //Midpoint of edge5
    maps[5].X = (p5.X + p6.X) / 2.0f;
    maps[5].Y = (p5.Y + p6.Y) / 2.0f;
    maps[5].Z = (p5.Z + p6.Z) / 2.0f;

    //Midpoint of edge6
    maps[6].X = (p6.X + p7.X) / 2.0f;
    maps[6].Y = (p6.Y + p7.Y) / 2.0f;
    maps[6].Z = (p6.Z + p7.Z) / 2.0f;

    //Midpoint of edge7
    maps[7].X = (p7.X + p0.X) / 2.0f;
    maps[7].Y = (p7.Y + p0.Y) / 2.0f;
    maps[7].Z = (p7.Z + p0.Z) / 2.0f;

    //Midpoint of edge8
    maps[8].X = (p4.X + p0.X) / 2.0f;
    maps[8].Y = (p4.Y + p0.Y) / 2.0f;
    maps[8].Z = (p4.Z + p0.Z) / 2.0f;

    //Midpoint of edge9
    maps[9].X = (p5.X + p1.X) / 2.0f;
    maps[9].Y = (p5.Y + p1.Y) / 2.0f;
    maps[9].Z = (p5.Z + p1.Z) / 2.0f;

    //Midpoint of edge10
    maps[10].X = (p2.X + p6.X) / 2.0f;
    maps[10].Y = (p2.Y + p6.Y) / 2.0f;
    maps[10].Z = (p2.Z + p6.Z) / 2.0f;

    //Midpoint of edge11
    maps[11].X = (p7.X + p3.X) / 2.0f;
    maps[11].Y = (p7.Y + p3.Y) / 2.0f;
    maps[11].Z = (p7.Z + p3.Z) / 2.0f;

    //Draw the triangles that were found.  There can be up to five per cube
    for (GLint i = 0; i < 5; i++)
    {
        if (connections[3 * i] < 0)
            break;

        GLint x1, x2, x3; 
        x1 = connections[3*i];
        x2 = connections[3 * i + 1];
        x3 = connections[3 * i + 2];

        glColor3f(0,0,1); 
        glBegin(GL_TRIANGLES); 
            glVertex3f(maps[x1].X,maps[x1].Y,maps[x1].Z);
            glVertex3f(maps[x2].X, maps[x2].Y, maps[x2].Z);
            glVertex3f(maps[x3].X, maps[x3].Y, maps[x3].Z);
        glEnd(); 
    }
}

void DrawTriangles() {

    for (int i = 0; i < rows-1; i++) {
        for (int j = 0; j < cols - 1; j++) {
            for (int k = 0; k < depth - 1; k++) {
                Triangle(
                    surface[i][j][k],
                    surface[i+1][j][k],
                    surface[i+1][j][k+1],
                    surface[i][j][k+1],
                    surface[i][j+1][k],
                    surface[i+1][j+1][k],
                    surface[i+1][j+1][k+1],
                    surface[i][j+1][k+1]
                    ); 
            }
        }
    }
	
}
void Draw() {
	glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

	GLfloat V[8][3] = {
		{-0.5,0.5,0.5},
		{0.5,0.5,0.5},
		{0.5,-0.5,0.5},
		{-0.5,-0.5,0.5},

		{-0.5,0.5,-0.5},
		{0.5,0.5,-0.5},
		{0.5,-0.5,-0.5},
		{-0.5,-0.5,-0.5},
	};

	//Camera
	glLoadIdentity();
	gluLookAt(Cx,Cy,Cz,0,0,0,0,1,0);
	//Draw a cube
	Cube(V[0], V[1], V[2], V[3], V[4], V[5], V[6], V[7]);

	//Drawing Triangles
    DrawTriangles();

	glutSwapBuffers();
}

