struct GLPoint {
	GLfloat X;
	GLfloat Y;
	GLfloat Z;
	GLint value; 
};

//Global Variables
GLfloat Cx = 0.0f, Cy = 0.0f, Cz = 3.0f;
const GLint resolution = 5;
GLPoint surface[resolution][resolution][resolution];

auto cols = resolution;
auto rows = cols;
auto depth = cols;

void Setup() {

	GLfloat increment = 1.0 / (resolution - 1)*1.0;
	GLfloat x = -0.5f - increment, y = -0.5f - increment, z = -0.5f - increment;

	for (int i = 0; i < cols; i++) {
		x = x + increment;
		y = -0.5f - increment;
		for (int j = 0; j < rows; j++) {
			y = y + increment;
			z = -0.5f - increment;
			for (int k = 0; k < depth; k++) {
				z = z + increment;
				surface[i][j][k].value = rand() % 2;
				surface[i][j][k].X = x;
				surface[i][j][k].Y = y;
				surface[i][j][k].Z = z;
			}
		}
	}	
}


void my_init() {
	Setup();

	//Background Color
	glClearColor(0, 0, 0, 1);

	//Only 3D
	glEnable(GL_DEPTH_TEST);

	//Perspective view
	glMatrixMode(GL_PROJECTION);
	glLoadIdentity();
	glFrustum(-1, 1, -1, 1, 2, 10);
	glMatrixMode(GL_MODELVIEW);
}

void Key(unsigned char ch,int x,int y) {
	switch (ch)
	{
		case 'x' :
			Cx = Cx - 0.5f;
			break;
		case 'X':
			Cx = Cx + 0.5f;
			break;
		case 'y':
			Cy = Cy - 0.5f;
			break;
		case 'Y':
			Cy = Cy + 0.5f;
			break;
		case 'z':
			Cz = Cz - 0.5f;
			break;
		case 'Z':
			Cz = Cz + 0.5f;
			break;
        case 'N':
            Setup();
            break;
	}
	glutPostRedisplay();
}

